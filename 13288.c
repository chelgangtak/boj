#include <stdio.h>

int main(void) {
	char s[26][10]={"@","8","(","|)","3","#","6","[-]","|","_|","|<","1","[]\\/[]","[]\\[]","0","|D","(,)","|Z","$","']['","|_|","\\/","\\/\\/","}{","`/", "2"}, c;
	while(1) {
		scanf("%c", &c);
		if (c>='A'&&c<='Z') c=c-'A'+'a';
		if (c>='a'&&c<='z') printf("%s", s[c-'a']);
		else{
			printf("%c", c);
			if (c=='\n') return 0;
		}
	}
}
